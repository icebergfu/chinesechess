void Position::fromFen(const char *fen) {

  clearBoard();

  char c;
  int y = RANK_TOP;
  int x = FILE_LEFT;

  int i;
  int len = strlen(fen);
  for (i = 0; i < len; i++) {
    c = fen[i];
    //printf("i=%d %c\n", i, c);
    if (c == ' ') {
      break;
    }
    else if (c == '/') {
      x = FILE_LEFT;
      y++;
      if (y > RANK_BOTTOM) {
        break;
      }
    }
    else if (c >= '1' && c <= '9') {
      x += (c - '0');
    }
    else if (c >= 'A' && c <= 'Z') {
      if (x <= FILE_RIGHT) {
        int pt = CHAR_TO_PIECE(c);
        addPiece(COORD_XY(x, y), pt + 8);
        x++;
      } else {
        assert(0);
      }
    } else if (c >= 'a' && c <= 'z') {
      if (x <= FILE_RIGHT) {
        int pt = CHAR_TO_PIECE(c - 'a' + 'A');
        addPiece(COORD_XY(x, y), pt + 16);
        x++;
      } else {
        assert(0);
      }
    }
    else {
      //printf("\n??\n");
    }
  }

  i++;
  if (i >= len) {
    setIrrev();
    return;
  }

  c = fen[i];
  //printf("c = %c\n", c);
  if (c == 'b' || c== 'w') {
    sdPlayer = (c == 'b') ? 0 : 1;
    changeSide();
  }
  setIrrev();
}

void Position::generateMoves(std::vector<int> &mvs, std::vector<int> &vls) {
  int pcSelfSide = SIDE_TAG(sdPlayer);
  int pcOppSide = OPP_SIDE_TAG(sdPlayer);
  int i, j;
  int sqSrc;
  for (sqSrc = 0; sqSrc < 256; sqSrc++) {
    int pcSrc = squares[sqSrc];
    if ((pcSrc & pcSelfSide) == 0) {
      continue;
    }
    int this_pc = pcSrc - pcSelfSide;
    switch (this_pc) {

      case PIECE_KING:
        for (i = 0; i < 4; i++) {
          int sqDst = sqSrc + KING_DELTA[i];
          if (!IN_FORT(sqDst)) {
            continue;
          }
          int pcDst = squares[sqDst];
          if ((pcDst & pcOppSide) != 0) {
            mvs.push_back(MOVE(sqSrc, sqDst));
            vls.push_back(MVV_LVA(pcDst, 5));
          }
        }
        break;

      case PIECE_ADVISOR:
        for (i = 0; i < 4; i++) {
          int sqDst = sqSrc + ADVISOR_DELTA[i];
          if (!IN_FORT(sqDst)) {
            continue;
          }
          int pcDst = squares[sqDst];
          if ((pcDst & pcOppSide) != 0) {
            mvs.push_back(MOVE(sqSrc, sqDst));
            vls.push_back(MVV_LVA(pcDst, 1));
          }
        }
        break;

      case PIECE_BISHOP:
        for (i = 0; i < 4; i++) {
          int sqDst = sqSrc + ADVISOR_DELTA[i];
          if (!IN_BOARD(sqDst) || !HOME_HALF(sqDst, sdPlayer) ||squares[sqDst] != 0) {
            continue;
          }
          sqDst += ADVISOR_DELTA[i];
          int pcDst = squares[sqDst];
          if ((pcDst & pcOppSide) != 0) {
            mvs.push_back(MOVE(sqSrc, sqDst));
            vls.push_back(MVV_LVA(pcDst, 1));
          }
        }
        break;

      case PIECE_KNIGHT:
        for (i = 0; i < 4; i++) {
          int sqDst = sqSrc + KING_DELTA[i];
          if (squares[sqDst] != 0) {
            continue;
          }
          for (j = 0; j < 2; j++) {
            sqDst = sqSrc + KNIGHT_DELTA[i][j];
            if (!IN_BOARD(sqDst)) {
              continue;
            }
            int pcDst = squares[sqDst];
            if ((pcDst & pcOppSide) != 0) {
              mvs.push_back(MOVE(sqSrc, sqDst));
              vls.push_back(MVV_LVA(pcDst, 1));
            }
          }
        }
        break;

      case PIECE_ROOK:
        for (i = 0; i < 4; i++) {
          int delta = KING_DELTA[i];
          int sqDst = sqSrc + delta;
          while (IN_BOARD(sqDst)) {
            int pcDst = squares[sqDst];
            if (pcDst == 0) {
              // mvs.push_back(MOVE(sqSrc, sqDst));
            } else {
              if ((pcDst & pcOppSide) != 0) {
                mvs.push_back(MOVE(sqSrc, sqDst));
                vls.push_back(MVV_LVA(pcDst, 4));
              }
              break;
            }
            sqDst += delta;
          }
        }
        break;

      case PIECE_CANNON:
        for (i = 0; i < 4; i++) {
          int delta = KING_DELTA[i];
          int sqDst = sqSrc + delta;
          while (IN_BOARD(sqDst)) {
            int pcDst = squares[sqDst];
            if (pcDst == 0) {
              // mvs.push_back(MOVE(sqSrc, sqDst));
            } else {
              break;
            }
            sqDst += delta;
          }
          sqDst += delta;
          while (IN_BOARD(sqDst)) {
            int pcDst = squares[sqDst];
            if (pcDst > 0) {
              if ((pcDst & pcOppSide) != 0) {
                mvs.push_back(MOVE(sqSrc, sqDst));
                vls.push_back(MVV_LVA(pcDst, 4));
              }
              break;
            }
            sqDst += delta;
          }
        }
        break;

      case PIECE_PAWN:
        {
          int sqDst = SQUARE_FORWARD(sqSrc, sdPlayer);
          if (IN_BOARD(sqDst)) {
            int pcDst = squares[sqDst];
            if ((pcDst & pcOppSide) != 0) {
              mvs.push_back(MOVE(sqSrc, sqDst));
              vls.push_back(MVV_LVA(pcDst, 2));
            }
          }
        }
        if (AWAY_HALF(sqSrc, sdPlayer)) {
          for (int delta = -1; delta <= 1; delta += 2) {
            int sqDst = sqSrc + delta;
            if (IN_BOARD(sqDst)) {
              int pcDst = squares[sqDst];
              if ((pcDst & pcOppSide) != 0) {
                mvs.push_back(MOVE(sqSrc, sqDst));
                vls.push_back(MVV_LVA(pcDst, 2));
              }
            }
          }
        }
        break;

      default:
        break;
    }
  }
}

